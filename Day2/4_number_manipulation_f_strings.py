# You can round numbers using the round function:
print(round(8 / 3))

# You can also use this to specify decimal places you want to round to:
print(round(8 / 3, 2))

# You can also store calculations in a variable and reassign them with operations;

result = 4 / 2
result /= 2
print(result)

