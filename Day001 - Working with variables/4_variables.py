# Variables allow you to store data within a reusable namespace. You can then refer to this variable within your code.

name = input("What is your name? ")
print(name)

# Variables can be changed

name = "Riajul"
print(name)

name = "James"
print(name)

# Naming variables: Should be able to look at code after some time and know what the variable is. Can have multiple
# words by using underscores. You can use numbers but cannot start with numbers. You should not use built-in function
# names as variable names as this can cause confusion such as "print".